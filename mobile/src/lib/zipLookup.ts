/**
 * Look up city and state from ZIP code
 */
export function lookupZip(zipCode: string): ZipLookupResult {
  const cleanZip = zipCode.trim().replace(/\D/g, '');

  if (cleanZip.length !== 5) {
    return { city: '', state: '', valid: false };
  }

  const location = ZIP_DATABASE[cleanZip];

  if (location) {
    return {
      city: location.city,
      state: location.state,
      valid: true,
    };
  }

  return { city: '', state: '', valid: false };
}

/**
 * Check if a ZIP code is valid
 */
export function isValidZip(zipCode: string): boolean {
  const cleanZip = zipCode.trim().replace(/\D/g, '');
  return cleanZip.length === 5 && ZIP_DATABASE[cleanZip] !== undefined;
}
