// Get all make names as a simple array
export function getAllMakeNames(): string[] {
  return VEHICLE_MAKES.map((m) => m.name);
}

// Search makes by partial name (for autocomplete)
export function searchMakes(query: string, limit = 3): string[] {
  if (!query.trim()) return [];

  const q = query.toLowerCase().trim();
  const results: { name: string; score: number }[] = [];

  for (const make of VEHICLE_MAKES) {
    const nameLower = make.name.toLowerCase();

    // Exact match
    if (nameLower === q) {
      results.push({ name: make.name, score: 100 });
      continue;
    }

    // Starts with query
    if (nameLower.startsWith(q)) {
      results.push({ name: make.name, score: 80 + (q.length / nameLower.length) * 10 });
      continue;
    }

    // Contains query
    if (nameLower.includes(q)) {
      results.push({ name: make.name, score: 50 + (q.length / nameLower.length) * 10 });
      continue;
    }

    // Fuzzy match (characters in order)
    let matchIndex = 0;
    for (const char of nameLower) {
      if (matchIndex < q.length && char === q[matchIndex]) {
        matchIndex++;
      }
    }
    if (matchIndex === q.length) {
      results.push({ name: make.name, score: 30 + (q.length / nameLower.length) * 10 });
    }
  }

  // Sort by score descending and return top results
  return results
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map((r) => r.name);
}

// Get models for a specific make and year
export function getModelsForMakeAndYear(makeName: string, year: number): string[] {
  const make = VEHICLE_MAKES.find((m) => m.name.toLowerCase() === makeName.toLowerCase());
  if (!make) return [];

  return make.models
    .filter((model) => model.years.includes(year))
    .map((model) => model.name)
    .sort();
}

// Get all models for a make (all years)
export function getAllModelsForMake(makeName: string): string[] {
  const make = VEHICLE_MAKES.find((m) => m.name.toLowerCase() === makeName.toLowerCase());
  if (!make) return [];

  return make.models.map((model) => model.name).sort();
}

// Validate if a make exists
export function isValidMake(makeName: string): boolean {
  return VEHICLE_MAKES.some((m) => m.name.toLowerCase() === makeName.toLowerCase());
}

// Get the correct capitalization for a make
export function getCorrectMakeName(makeName: string): string | null {
  const make = VEHICLE_MAKES.find((m) => m.name.toLowerCase() === makeName.toLowerCase());
  return make?.name ?? null;
}

// Generate year options (last 15 years)
export function getYearOptions(): number[] {
  const currentYear = new Date().getFullYear();
  const years: number[] = [];
  for (let year = currentYear + 1; year >= currentYear - 15; year--) {
    years.push(year);
  }
  return years;
}

